#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new_node;
	void	*new_con;

	new_list = NULL;
	while (lst)
	{
		new_con = f(lst->content);
		new_node = ft_lstnew(new_con);
		if (!new_node)
		{
			if (del)
				del(new_con);
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new_node);
		lst = lst->next;
	}
	return (new_list);
}
